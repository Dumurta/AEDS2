package AEDS2.tarefas.labs;

public class Espelho {
    public static int reverter(int[] array)
    {
        int s2;
        int tamanho = array.length;
        for(int i = tamanho; i >= tamanho; i--)
        {
            int j = 0;
            s2[j] += array[i];
            j++;
        }
    }
}
